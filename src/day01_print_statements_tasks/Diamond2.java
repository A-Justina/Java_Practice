package day01_print_statements_tasks;

public class Diamond2 {
    public static void main(String[] args) {

        System.out.println("     ^");
        System.out.println("    / \\");
        System.out.println("   / * \\");
        System.out.println("  / * * \\");
        System.out.println(" / * * * \\");
        System.out.println(" --------- ");
        System.out.println(" \\ * * * /");
        System.out.println("  \\ * * /");
        System.out.println("   \\ * /");
        System.out.println("    \\ /");
        System.out.println("     V");
    }
}
